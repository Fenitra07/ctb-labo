          <h2>Conditions de prélèvement</h2>
          <!-- <p style="text-align: center;">En cette période de pandémie, le laboratoire CTB propose 2 différents tests pour le Covid-19.</p> -->
        </div>
      
        <div class="faq-list">

          <li data-aos="fade-up" data-aos-delay="100">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-11" class="collapsed">I. Les analyses strictement à jeun  :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-11" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Pour certaines analyses, il est indispensable d’être à jeun, c’est-à-dire que vous ne devez prendre ni de la nourriture solide, ni liquide depuis au moins 10 heures de temps. Sont concernées dans ces tests : <br>
                  <i>
                    • La glycémie <br>
                    • La cholestérolémie (fractions HDL et LDL) <br>
                    • La triglycéridémie <br>
                    • L’uricémie <br>
                    • La calcémie <br>
                    • La phosphorémie <br>
                    • Iget <br>
                    • CLA30
                  </i>
                  </p>
              </div>
            </li>

            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-2" class="collapsed">II.  Les épreuves de l’hypoglycémie provoquée :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-2" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Pour le dépistage du diabète gestationnel (HGPO), les conditions de jeun total doivent être respectées. Mais en plus, le patient doit rester au laboratoire pendant au moins 2h de temps. Cela, parce que le prélèvement se passe en plusieurs étapes : <br>
                  <i>
                    • Un premier prélèvement pour la glycémie à jeun <br>
                    • Ingestion de 75g de sucre dilué dans 500 ml d’eau <br>
                    • Un deuxième prélèvement 1 h après ingestion de la solution sucrée <br>
                    • Un troisième prélèvement 2 h après ingestion de la solution sucrée
                  </i>
                  </p>
                  <p style="text-align: justify;">
                  D’autre part, le médecin peut demander le test O’ Sullivan comme test de diagnostic de diabète gestationnel. Le patient doit également être totalement à jeun depuis la veille et prévoir 1h de temps pour effectuer le test. Il se déroule également en plusieurs étapes : <br>
                  <i>
                    • Un premier prélèvement pour la glycémie à jeun <br>
                    • Ingestion de 50g de sucre dilué dans 500 ml d’eau <br>
                    • Un deuxième prélèvement 1 h après ingestion de la solution sucrée
                  </i>
                  </p>
              </div>
            </li>


            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-3" class="collapsed">III. Les analyses où il est préférable d’être à jeun :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-3" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Pour d’autres analyses, il est préférable d’être à jeun, bien que ce ne soit pas obligatoire. Cela concerne : <br>
                  <i>
                    • Le Fer <br>
                    • La créatininémie <br>
                    • La CRP <br>
                    • L’ionogramme <br>
                    • Les enzymes hépatiques : ALAT/ASAT (transaminases), GGT, Bilirubines (BTC), Phosphatases Alcalines (PAL) <br>
                    • Les enzymes cardiaques : Lactico-déshydrogénase (LDH), CK-MB (CKMB) <br>
                    • La numération (NFS) <br>
                    • La vitesse de SEDIMENTATION (VS) <br>
                    • Les tests de coagulation : Taux de Prothrombine (TP) + INR, Fibrinogène ou Facteur I (FIB), Temps de Céphaline Activée ou Temps de Quick (TCA), Temps de Saignement (TS) <br>
                    • L’électrophorèse des protéines sériques (ELPR)
                  </i>
                  </p>
              </div>
            </li>
          
          
         
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-4" class="collapsed">IV.  Les analyses qui ne nécessitent pas d’être à jeun  :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-4" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Le jeun est inutile pour les analyses sérologiques ainsi que les marqueurs tumoraux, car l’alimentation n’influence pas les résultats. Vous n’avez pas besoin d’être à jeun pour l’analyse de : <br>
                  <i>
                    • La syphilis <br>
                    • Le VIH <br>
                    • Les hépatites A, B et C <br>
                    • La toxoplasmose <br>
                    • La rubéole <br>
                    • La fièvre typhoïde <br>
                    • La salmonellose <br>
                    • L’amibiase <br>
                    • La bilharziose <br>
                    • La cysticercose <br>
                    • Les marqueurs tumoraux : PSA, ACE, AFP, CA125, CA153, CA199
                  </i>
                  </p>
              </div>
            </li>
          
          
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-5" class="collapsed">V. Recueil des urines de 24 heures :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-5" class="collapse" data-bs-parent=".faq-list">
                <i>
                • Au lever, éliminer les premières urines dans les toilettes. <br>
                • Commencer le recueil des urines sur la prochaine miction en notant bien l’heure du début. <br>
                • Continuer à recueillir toutes les urines pendant les 24 heures qui suivent. C’est-à dire jusqu’à la même heure le lendemain.
              </i> 
                <p style="text-align: justify;">
                 Utilisez des bouteilles bien propres et n’ayant pas contenu de liquide sucré pour le recueil. Si possible, gardez les urines recueillies dans un endroit frais. Quand vous avez complétez les 24 heures de recueil, apportez rapidement toutes les urines au laboratoire.
                </p>
                <p style="text-align: justify;">
                  Le dosage de plusieurs paramètres biochimiques tels que : l’acide urique urinaire, le calcium urinaire, le cortisol urinaire, la créatinine urinaire, le glycosurie, l’ionogramme urinaire, le magnésium urinaire, microalbuminurie, le phosphore urinaire, protéinurie et urée urinaire ne se font que sur les urines de 24 heures.  
                </p>
              </div>
            </li>
         
            
          
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-6" class="collapsed">VI.  Recueil d’urines pour le « COMPTE D’ADDIS » (H.L.M.) :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-6" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Pour le compte d’ADDIS ou culot HLM, il faut procéder comme suit : <br>
                  <i>
                   • Se réveiller 3 heures plus tôt que d’habitude <br>
                   • Se débarrasser des premières urines du matin dans les toilettes <br>
                   • Boire un grand verre d’eau, au plus 250ml <br>
                   • Mettre un réveil pour 3 heures, puis se rendormir <br>
                   • Au réveil, recueillir toutes les urines dans un flacon ou une bouteille bien propre et n’ayant pas contenu de liquide sucré.
                   </i>
                </p>
              </div>
            </li>
          
            
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-7" class="collapsed">VII. Recueil de sperme en vue d’un spermogramme ou d’une spermoculture :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-7" class="collapse mt-2" data-bs-parent=".faq-list">
                <b>Les conditions à respecter pour le recueil :</b>
                <p style="text-align: justify;">
                  Le recueil de sperme s’effectue uniquement au laboratoire afin d’éviter tout risque d’altération des spermatozoïdes pendant le transport. <br>
                  Si toutefois, vous devez l’effectuer à domicile, il faut acheminer le recueil dans un délai de moins de 30 minutes, à l’abri de la lumière et à une température de 37 °C. <br>
                  Il faut respecter une abstinence sexuelle d’au moins 3 jours pour pouvoir faire le recueil. 
                  S’il s’agit d’un recueil de sperme pour un spermoculture, il faut respecter un délai de 10 jours après un traitement antibiotique avant de le réaliser. Par ailleurs, le recueil peut être réaliser à domicile pour cet examen. 
                </p>
                <div class="mt-2"></div>
                <b>Le recueil proprement dit :</b>
                <p style="text-align: justify;">
                  Uriner avant de réaliser le recueil.<br>
                  Bien se laver les mains et la verge avec de l’eau<br>
                  Pratiquer la masturbation pour faire le recueil dans un flacon stérile <br>
                  Le laboratoire CTB peut recevoir une demande de spermogramme du lundi au vendredi, de 7h à 10h. 
                </p>
              </div>
            </li>
            
    
         
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-8" class="collapsed">VIII.  Examen cytobactériologique des Urines (ECBU) :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-8" class="collapse" data-bs-parent=".faq-list">
                <p style="text-align: justify;">
                  Le recueil doit se faire sur les premières urines du matin et dans un flacon stérile fournit par le laboratoire CTB.   
                </p>
                <p style="text-align: justify;">
                  <b>Comment procéder :</b>  <br> 
                   • Bien se laver les mains  <br>
                   • Procéder à la toilette intime habituelle <br>
                   • Eliminer le premier jet <br>
                   • Recueillir dans le flacon stérile le jet du milieu en prenant soin de ne pas toucher les rebords du flacon <br>
                   • Fermer le flacon hermétiquement et présenter l’échantillon à l’accueil du laboratoire dans les plus brefs délais 
                </p>
                <p style="text-align: justify;">
                  <b>Si le patient est sondé à domicile ou en milieu hospitalier :</b> <br>
                  Si possible, faire le recueil uniquement lors du changement de la sonde… <br>
                  Si ce n’est pas possible, commencer par clamper la tubulure en aval afin de permettre un recueil dans une nouvelle miction. Dès qu’une quantité d’urines jugée suffisante s’accumule dans la tubulure, procéder comme pour un prélèvement à la seringue. Désinfecter la tubulure, puis prélever les urines avec une aiguille et une seringue stérile. Transférer les urines recueillies dans le flacon stérile fourni par le laboratoire. Apporter l’échantillon au laboratoire dans les plus brefs délais.
                </p>
              </div>
            </li>
         

            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-9" class="collapsed">IX. Recommandations pour les prélèvements gynécologiques :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-9" class="collapse mt-2" data-bs-parent=".faq-list">
                <b>Pour un prélèvement vaginal ou cervico-vaginal :</b>
                <p style="text-align: justify;">
                  Pas de rapport sexuel au moins 72 heures avant le prélèvement. <br>
                  Pas de douche, ni de toilette vaginale le jour du prélèvement <br>
                  Pas de traitement antibiotique d’aucune sorte 10 jours avant le prélèvement <br>
                  Le prélèvement doit être fait en dehors des règles.
                </p>
                <div class="mt-2"></div>
                <b>Pour un prélèvement urétral :</b>     
                <p style="text-align: justify;">
                  Pas de rapport sexuel au moins 72 heures avant le prélèvement. <br>
                  Pas de douche, ni de toilette intime le jour du prélèvement <br>
                  Pas de traitement antibiotique d’aucune sorte 10 jours avant le prélèvement <br>
                  Eviter d’uriner avant le prélèvement
                </p>
              </div>
            </li> 
          
      
            <li data-aos="fade-up" data-aos-delay="100" style="margin-top: -20px;">
              <i class="bx bx-plus-circle icon-help"></i> <a data-bs-toggle="collapse" data-bs-target="#faq-list-10" class="collapsed">X. Autres prélèvements bactériologiques :<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="faq-list-10" class="collapse mt-2" data-bs-parent=".faq-list">
                <b>Examen cytobactériologique d’un crachat : </b>
                <p style="text-align: justify;">
                  Il est impératif que le recueil du crachat soit fait dans un flacon stérile fourni par le laboratoire. <br> 
                  Le recueil doit se faire avant de se brosser les dents et avant toute ingestion de liquide ou de nourriture. <br>
                  Pas de traitement antibiotique d’aucune sorte 10 jours avant le prélèvement
                </p>
                
                <div class="mt-2"></div>
                <b>Examen cytobactériologique d’un prélèvement de gorge : </b>
                <p style="text-align: justify;">
                  Il est impératif que le prélèvement soit fait dans au laboratoire CTB. <br>
                  Pas de se brossage de dents, ni d’ingestion de liquide et de nourriture avant le prélèvement. <br>
                  Pas de traitement antibiotique d’aucune sorte 10 jours avant le prélèvement
                </p>
                
                <div class="mt-2"></div>
                <b>Examen cytobactériologique d’une suppuration (pus) : </b>
                <p style="text-align: justify;">
                  Le prélèvement peut se faire au laboratoire à l’aide d’un écouvillon stérile <br>
                  Pas de traitement antibiotique d’aucune sorte 10 jours avant le prélèvement
                </p>
                
                <div class="mt-2"></div>
                <b>Examen cytobactériologique et cytochimique d’un liquide de ponction : </b>
                <p style="text-align: justify;">
                  Le prélèvement ne se fait pas au laboratoire, mais en milieu hospitalier. Pour l’examen cytobactériologique, le prélèvement doit être fait dans une seringue ou un flacon stérile. Il doit également se faire en dehors de tout traitement antibiotique durant l0 jours. 
                </p>
                
                <div class="mt-2"></div>
                <b>Recherche de BAAR :</b>
                <p style="text-align: justify;">
                  La recherche de BAAR sur crachat se fait sur 3 échantillons pendant 3 jours. <br>
                  Le recueil doit se faire avant de se brosser les dents et avant toute ingestion de liquide ou de nourriture. <br>
                  L’échantillon doit être acheminé rapidement au laboratoire. 
                </p>
              </div>
            </li> 
                
